/*
Argumentos de las funciones


Hay dos maneras de pasar argumentos a una función mientras se llama a la función.

Por valor: Este método copia el valor real del argumento en el parámetro formal de la función. 
En este caso, podemos realizar cambios en el parámetro dentro de la función sin que ello afecte al argumento.

Por referencia: Este método copia la referencia del argumento en el parámetro formal. 
Dentro de la función, la referencia se utiliza para acceder al argumento real utilizado en la llamada. 
Esto significa que cualquier cambio realizado en el parámetro afecta al argumento.
Por defecto, C++ utiliza la llamada por valor para pasar los argumentos.


 */