/*
Qué es una clase


Los objetos se crean mediante clases, que son en realidad el punto central de la programación orientada a objetos.

La clase describe lo que será el objeto, pero está separada del objeto mismo.
En otras palabras, una clase puede describirse como el plano, la descripción o la definición de un objeto.
Se puede utilizar la misma clase como plano para crear varios objetos diferentes. Por ejemplo, como preparación para la creación de un nuevo edificio, el arquitecto crea un plano, que se utiliza como base para la construcción de la estructura. Ese mismo plano puede utilizarse para crear varios edificios.

La programación funciona de la misma manera. Primero definimos una clase, que se convierte en el plano para crear objetos.

Cada clase tiene un nombre y describe atributos y comportamiento.

En programación, el término tipo se utiliza para referirse al nombre de una clase: estamos creando un objeto de un tipo determinado.
Los atributos también se denominan propiedades o datos.

Métodos


Método es otro término para el comportamiento de una clase. Un método es básicamente una función que pertenece a una clase.
Los métodos son similares a las funciones - son bloques de código que son llamados, y también pueden realizar acciones y devolver valores.



Un ejemplo de clase


Por ejemplo, si estamos creando un programa bancario, podemos dar a nuestra clase las siguientes características:
nombre: CuentaBancaria
atributos: accountNumber, balance, dateOpened
comportamiento: abrir(), cerrar(), depositar()

La clase especifica que cada objeto debe tener los atributos y el comportamiento definidos. Sin embargo, no especifica cuáles son los datos reales; sólo proporciona una definición.

Una vez que hemos escrito la clase, podemos pasar a crear objetos que se basan en esa clase.
Cada objeto se llama instancia de una clase. El proceso de creación de objetos se llama instanciación.
Cada objeto tiene su propia identidad, datos y comportamiento.
 */