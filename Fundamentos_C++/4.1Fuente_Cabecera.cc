/*
Fuente y cabecera


Para usar nuestras clases en nuestro main, necesitamos incluir el archivo de cabecera.

Por ejemplo, para usar nuestra recién creada MyClass en main:

 */
#include <iostream>
#include "MyClass.h"
using namespace std;

int main() {
    MyClass obj;
}
/*
La cabecera declara "lo que" hará una clase (o lo que se esté implementando), mientras que el archivo fuente cpp define "cómo" realizará esas funciones.
 */